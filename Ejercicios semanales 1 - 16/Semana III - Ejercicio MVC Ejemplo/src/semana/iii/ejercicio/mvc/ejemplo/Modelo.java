package semana.iii.ejercicio.mvc.ejemplo;

// Implementar un programa de calculadora con operaciones básicas 

public class Modelo {
    
    // Funciones que corresponden a la representación de los datos
    // En este caso el sistema no tiene un método de almacenamientos permanentes
     
    public int sumar(int a, int b){
        return a+b;
    }
    public int restar(int a, int b){
        return a-b;
    }
    public int mult(int a, int b){
        return a*b;
    }
    public int div(int a, int b){
        return a/b;
    }
    

    }
